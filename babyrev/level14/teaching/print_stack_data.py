#!/usr/bin/env python3

# From STACK[0x4] to STACK[0xe]
s = "0x45 0x4e 0x54 0x45 0x52 0x20 0x4b 0x45 0x59 0x3a 0x20"

for element in s.split():
    print(chr(int(element, 16)), end='')
print()

# From STACK[0xc] to STACK[0x16]
s = "0x49 0x4e 0x43 0x4f 0x52 0x52 0x45 0x43 0x54 0x21 0xa"

for element in s.split():
    print(chr(int(element, 16)), end='')
print()
